#!/bin/bash
set -e

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
    CREATE USER $GOGOMEDIA_DB_USER WITH PASSWORD '$GOGOMEDIA_DB_PASS';

    CREATE DATABASE $GOGOMEDIA_DB_NAME;
    GRANT ALL PRIVILEGES ON DATABASE $GOGOMEDIA_DB_NAME TO $GOGOMEDIA_DB_USER;

    CREATE DATABASE $GOGOMEDIA_TEST_DB_NAME;
    GRANT ALL PRIVILEGES ON DATABASE $GOGOMEDIA_TEST_DB_NAME TO $GOGOMEDIA_DB_USER;
EOSQL
